const names = [
  'Анна', 'Иван', 'Мария', 'Петр', 'Елена',
  'Дмитрий', 'Ольга', 'Алексей', 'Наталья', 'Сергей'
];

const photosDataBase = [];
let commentId = 0;

// дополнительные функции
const getRandomInt = (a, b) => Math.floor(Math.random() * (b - a + 1)) + a;

// описываем функции добавления комментариев.
const getMessage = () => {
  let message = '';
  for (let i = 0; i < 2; i++) {
    const commentsDataIndex = getRandomInt(0, commentsData.length - 1);
    if(message !== commentsData[commentsDataIndex]) { //если такое сообщение уже есть, то будет одно, если нет - то два.
      message += commentsData[commentsDataIndex];
    }
  }
  return message;
};

const getComment = () => ({
  id: commentId++,
  avatar: `img/avatar-${getRandomInt(1, AVATAR_MAX_NUM)}.svg`,
  message: getMessage(),
  name: names[getRandomInt(0, names.length - 1)]
});

const getComments = () => {
  const photoComments = [];
  for (let i = 0; i < getRandomInt(0, COMMENTS_MAX_COUNT); i++) {
    photoComments.push(getComment());
  }
  return photoComments;
};

// описываем функции добавления фотографий.
const addPhoto = (index) => ({
  id: index,
  url: `photos/${index + 1}.jpg`,
  description: photoDescriptions[getRandomInt(0, photoDescriptions.length - 1)],
  likes: getRandomInt(Likes.MIN, Likes.MAX),
  comments: getComments()
});

const addPhotos = () => {
  for (let i = 0; i < PHOTO_COUNT; i++) {
    photosDataBase.push(addPhoto(i));
  }
};

// запускаем функцию.
addPhotos();
